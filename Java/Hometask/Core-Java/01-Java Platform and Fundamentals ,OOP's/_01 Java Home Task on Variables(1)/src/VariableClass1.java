/*	Problem Statement
 Daniel has 3 apples; Amber has 2 apples.
 How many apples do Daniel and Amber have together?
 */

public class VariableClass1 {
    public  static void main(String []args)
    {
        int danialApples = 3;  // for Denial Apples count
        int amberApples  = 2;  // for Amber Apples count

        /*
         * Together Danial and Amber has the following total apples
         */
        int totalApples = danialApples+amberApples;
        System.out.print("Total Apples : "+totalApples);
    }

}
